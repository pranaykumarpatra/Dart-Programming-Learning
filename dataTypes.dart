void main() {
  int a = 5;
  print(a);
  a = 9;
  print(a);
  //integer part ends here

  var b = 7;
  print(b);
  b = 8;
  print(b);

  dynamic c = 9;
  print(c);
  c = 'pranay';
  print(c);
  c = 0.77;
  print(c);

  final d = 7;
  print(d);
  final int e = 8;
  print(e);

  const f = 9;
  print(f);
  const g = 10;
  print(g);
}
